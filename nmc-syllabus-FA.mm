<node TEXT="Reading response">
<node TEXT="The per-class response (~200 words) is a mandatory assignment. You are required to submit your response before 8 AM of the day of the class. Your response should contribute to your understanding in that it prompts you to make sense of the texts and to add your own critical insights to what you&#x2019;ve read. These responses also contribute to a much richer and more interesting class discussion. You should see this as an opportunity to practice your writing skills, so do not treat your response as you would an e-mail to your friends.">
