<node TEXT="In order to receive credit for a response, it must be complete, thoughtful, and on time. You may draw upon your own experience, but only if it highlights a thorough understanding of the text."/>
<node TEXT="Five &quot;freebies&quot; are given on the responses: those lowest grades, including absences, will be dropped from the grade. Use these wisely. Even if you don&apos;t prepare a written response, you will still be expected to participate in class. Also, even if you know you will be absent from class, you may still send your response."/>
