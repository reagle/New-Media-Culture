<node TEXT="Rubric">
<node TEXT="In this class&apos; grading scheme, a &quot;B,&quot; for example, is not a subtraction from an initial state of an &quot;A,&quot; but rather recognition of good and thorough work."/>
<node TEXT="A = Excellent. Writing demonstrates impressive understanding of readings, discussions, themes and ideas. Written work is fluid, clear, analytical, well-organized and grammatically polished. Reasoning and logic are well-grounded and examples precise."/>
<node TEXT="B = Good. Work demonstrates a thorough and solid understanding of readings, discussions, themes and ideas. Written work is clear and competent, but is somewhat general, a bit vague, or otherwise lacking in precision. While analytical, writing presents more description than analysis. Arguments are solid but not thoroughly original or polished."/>
<node TEXT="C = Fair. Work demonstrates a somewhat fragmented understanding of readings, discussions, themes and ideas. Shows acquaintance with readings and ideas, but not intellectual engagement. Written work is choppy and argument somewhat difficult to follow, examples are vague or irrelevant, and ideas are imprecise. Work veers toward underdeveloped ideas, off-topic sources or examples, personal anecdotes, creative writing, memoir, etc."/>
<node TEXT="D = Unsatisfactory. Work demonstrates little understanding or even acquaintance with readings, discussions, themes and ideas. Written work is choppy, fractured and unclear. Argument follows little logical development, or work presents little discernible argument whatsoever."/>
<node TEXT="F = Failure / Unacceptable. Work does not demonstrate understanding of topics, ideas and readings. This is also the grade for work not submitted and plagiarized work."/>
<node LINK="../../2007/teaching/student-evaluation.html" TEXT="See Student Evaluations for more"/>
</node>
<node TEXT=" Students will often ask if I grade on a curve. If everyone did excellently -- something I would like to see and help make happen -- that would correspondingly be reflected. However, assessed grades typically follow a &quot;curved&quot; distribution: typically some work is excellent, a minority is unsatisfactory, and most is good."/>
